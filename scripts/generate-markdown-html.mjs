import fs from 'fs';
import path from 'path';
import { unified } from 'unified';
import remarkParse from 'remark-parse';
import remarkRehype from 'remark-rehype';
import rehypeStringify from 'rehype-stringify';
import rehypeSanitize from 'rehype-sanitize';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const publicDir = path.join(__dirname, '../public');
const srcDir = path.join(__dirname, '../src');

// List of markdown files to pre-render
const markdownFiles = [
  { source: 'api.md', exportName: 'apiHtml' },
  { source: 'json.md', exportName: 'jsonHtml' }
];

async function generateMarkdownHtml() {
  console.log('Generating Markdown HTML...');

  // Create processor
  const processor = unified()
    .use(remarkParse)
    .use(remarkRehype, { allowDangerousHtml: true })
    .use(rehypeSanitize)
    .use(rehypeStringify, { allowDangerousHtml: true });

  let exports = [];

  for (const { source, exportName } of markdownFiles) {
    const sourcePath = path.join(publicDir, source);

    try {
      // Read markdown file
      const markdownContent = fs.readFileSync(sourcePath, 'utf8');

      // Convert to HTML
      const { value: htmlContent } = await processor.process(markdownContent);

      // Escape HTML string for JavaScript
      const escapedHtml = JSON.stringify(htmlContent);

      exports.push(`export const ${exportName} = ${escapedHtml};`);

      console.log(`✓ Generated ${exportName} from ${source}`);
    } catch (error) {
      console.error(`✗ Failed to process ${source}:`, error.message);
      process.exit(1);
    }
  }

  // Write TypeScript file
  const outputPath = path.join(srcDir, 'fabritor/UI/panel/ApiPanel/markdownHtml.ts');
  const content = `// Auto-generated by scripts/generate-markdown-html.js
// Do not edit this file directly

${exports.join('\n\n')}
`;

  fs.writeFileSync(outputPath, content, 'utf8');
  console.log(`✓ Generated ${outputPath}`);
  console.log('Markdown HTML generation complete!');
}

generateMarkdownHtml().catch((error) => {
  console.error('Error during generation:', error);
  throw error;
});
